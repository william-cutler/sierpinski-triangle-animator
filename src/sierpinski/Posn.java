package sierpinski;

/** A structure of integer, 2D Cartesian coordinates */
public class Posn {
  public double x;
  public double y;

  /**
   * Constructs this from the given x and y coordinate.
   *
   * @param x x-coordinate
   * @param y y-coordinate
   */
  public Posn(double x, double y) {
    this.x = x;
    this.y = y;
  }
}
